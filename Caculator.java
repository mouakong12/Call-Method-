 class Caculator{
    public static int plus(int a,int b) {
       return (a+b);
    }
    public static int minus(int c,int d) {
        return (c-d);
    }
    public static double multiply(double e,double f) {
        return (e*f);
    }
    public static double division(double g,double h){
        return (g/h);
    }
}
